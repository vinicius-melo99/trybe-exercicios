const a = 10, b = 2;
let soma, sub, multi, divisao, mod;

soma = a + b;
console.log(`O resultado de ${a} + ${b} = ${soma}`);

sub = a - b;
console.log(`O resultado de ${a} - ${b} = ${sub}`);

multi = a * b;
console.log(`O resultado de ${a} x ${b} = ${multi}`);

divisao = a / b;
console.log(`O resultado de ${a} / ${b} = ${divisao}`);

mod = a % b;
console.log(`O resultado de ${a} % ${b} = ${mod}`);